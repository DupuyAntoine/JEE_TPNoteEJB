package tpnote;

import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

/**
 *
 * @author AdminEtu
 */
@Stateless
public class EtudiantFacade extends AbstractFacade<Etudiant> implements EtudiantFacadeLocal {

    @PersistenceContext(unitName = "tpnotePU")
    private EntityManager em;

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

    public EtudiantFacade() {
        super(Etudiant.class);
    }

    @Override
    public Etudiant findByLogin(String login) {
        List<Etudiant> res = em.createQuery("select Object(e) from Etudiant e where e.login = ?1").setParameter(1, login).getResultList();
        return res.get(0);
    }
    
}
